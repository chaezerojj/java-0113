package section04;

public class EX04_10 {

	public static void main(String[] args) {
		int num = 4;
		
		switch (num) {
		case 1:
			System.out.println("num은 1입니다.");
			break;
		case 7:
			System.out.println("num은 7입니다.");
			break;
		default:
			System.out.println("num은 1과 7 모두 아닙니다.");
			break;
			
		}
	}

}
