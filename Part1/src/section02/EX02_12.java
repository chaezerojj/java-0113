package section02;

public class EX02_12 {

	public static void main(String[] args) {
		byte num1 = 0110;
		short num2 = 0117;
		int num3 = 0x46;
		long num4 = 69L;
		
		System.out.println(num1); // 72 => 0110 십진수로 72
		System.out.println(num2); // 79 => 0117 십진수로 79
		System.out.println(num3); // 70 => 0x46 십진수로 70
		System.out.println(num4); // 69 => 69L 십진수로 69
	}

}
